# ArithmeticOperators.py
"""Basic arithmetic operations."""

# Data type
print('type(1) is', type(1))
print('type(1.0) is', type(1.0))
print("type('Word') is", type('Word'))

# Addition (+)
print('1 + 1 =', 1 + 1)

# Subtraction (-)
print('1 - 1 =', 1 - 1)

# Multiplication (*)
print('7 * 4 =', 7 * 4)

# Exponentiation (**)
print('2 ** 10 =', 2 ** 10)

# True Division (/)
print('7 / 4 =', 7 / 4, ', -13 / 4 =', -13 / 4)

# Floor Division (//)
print('7 // 4 =', 7 // 4, ', -13 // 4 =', -13 // 4)

# To calculate the square root, you can use the exponent 1/2 (that is, 0.5):
print('9 ** (1 / 2) =', 9 ** (1 / 2))

# Remainder Operator (%)
print('17 % 5 =', 17 % 5, ', 7.5 % 3.5 =', 7.5 % 3.5)
